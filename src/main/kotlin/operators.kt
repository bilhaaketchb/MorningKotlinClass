fun main(){
    //Arithmetic Operators

    var num1=60
    var num2=20
    println(num1+num2)
    println(num1-num2)
    println(num1*num2)
    println(num1/num2)
    println(num1%num2) //Modulus-it gives a remainder

    //Assignment Operators
    var x=6
    x+=5
    println(x)

    var y=10
    y-=3
    println(y)

    var k=10
    k*=3
    println(k)

    var m=10
    m/=3
    println(m)

    var n=68
    n%=5
    println(n)

    //Comparison Operators-helps you compare one value to another
    var b=20
    println(b==2)
    println(b<2)
    println(b>2)
    println(b<=2)
    println(b>=2)

    //logic Operators
    var mylogic=80
    println(mylogic<50 && mylogic==80) //And
    println(mylogic<50 || mylogic==80) //or
    println(!(mylogic<50 || mylogic==80)) //Not



}